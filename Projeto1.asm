ORG 0000H

START:
    MOV DPTR, #SEQUENCIA

LOOP:
    CLR A
    MOVC A, @A+DPTR
    MOV R0, #0

PROXIMO:
    MOV A, R0
    MOVC A, @A+DPTR
    MOV P1, A
    ACALL DELAY

    INC R0
    CJNE R0, #08, PROXIMO

    SJMP START

SEQUENCIA:
    DB 11111110b
    DB 10111111b
    DB 11111011b
    DB 11011111b
    DB 11101111b
    DB 11111101b
    DB 11110111b
    DB 01111111b

DELAY:
    MOV R6, #0FFh
    MOV R7, #0FFh
ATRASO:
    DJNZ R6, ATRASO
    DJNZ R7, ATRASO
    RET
